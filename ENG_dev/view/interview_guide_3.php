	<h1>Interview skills can be learned</h1>
	<p>
  	Now you have prepared yourself for the event. But you still need to perform in the interview.
  	When you go to an interview, you need to <b>sell yourself</b>.
  	The interview is your chance to show the company that you are the best person for the job.
	</p>
	<ul>
		<li>Again: finding out information about the job and the company, preparing questions to ask</li>
		<li>Have prepared a folder with relevant information (your covering letter, CV, original and copy of
			certificates and references)</li>
		<li>Remember to relax: be friendly and polite</li>
		<li>Don’t be critical of other companies or people</li>
		<li>At the end thank the employer for the interview and ask when they shall take the final decision</li>
	</ul>
	<p>
	Avoid monosyllabic responses and volunteer supporting information whenever possible, but be brief.
	Give practical examples of how you have shown commitment and motivation in the past.
	</p>

	<h2>The day of the interview</h2>
	<p>
  	Make sure you know where the interview is taking place and allow yourself plenty of time to get there.
  	If you're going to be late, call the firm and let them know (take the phone number with you).
  	Try and find out how long the interview will last, and how many people will be interviewing you.
	</p>

	<p>
  	Arrive a little early for the interview. Ten minutes spent in the reception will give you time to
  	collect your thoughts and a chance to read the firm's brochures and study recent press releases.
  	Listening to the receptionists and watching the comings and goings can provide a valuable insight
  	into the type of firm you might be joining.
	</p>

	<h2>Dress code</h2>
	<p>
   	Even if the organisation dress code is relaxed or casual, you should be dressed smartly for an interview.
   	You cannot be too formal for an interview, but you can certainly fail by not being smart enough.
   	First impressions count, and making an obvious effort to be smart shows that you are serious and want
   	this job.
