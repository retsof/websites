	<h1>Body language</h1>

	<p>
  	The success of communication is often non-verbal. So be aware of your body language.
  	And remember, that you never get a second chance to make a first impression.
	</p>
	<p>
  	Look in the mirror and check your posture. Your shoulders can communicate more than words.
  	Relax and have an open and confident attitude. A firm handshake and a natural smile can be practised.
  	Keep remembering, you've hardly said a word and your interviewer might already be thinking positive
  	about you!
	</p>
	<ul>
		<li>Smile, shake hands firmly and establish eye contact</li>
		<li>Address people as Mr, Mrs or Ms unless they say otherwise</li>
		<li>Wait to be asked to sit down</li>
		<li>Don’t smoke, eat, drink or chew gum</li>
	</ul>

	<p>
  	Sitting slouched in a chair looking around the room gives the impression that you are not really
  	interested or enthusiastic, whereas sitting slightly forward in your seat and engaging your interviewer’s
  	eyes gives the impression that you are interested.
	</p>
	<ul>
		<li>Be relaxed, but sit upright</li>
		<li>Do not  fiddle with your hair, clothes or pens - if you tend to fidget, keep your hands apart</li>
		<li>Be aware of your voice: the pace and intonation can contribute very much to your success in an interview</li>
		<li>Do a bit of breathing exercise before the interview, to find your own pace</li>
	</ul>

