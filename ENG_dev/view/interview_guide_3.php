<div class="ebSection">
	<h1>During the interview</h1>
	<p>
  	You're in control of what you let the interviewer know, so take responsibility during the interview:
	</p>
	<ul>
		<li>If you are being interviewed by more than one person, engage the whole panel when responding</li>
		<li>Be clear and concise</li>
		<li>Use positive language</li>
		<li>Even if doubts are setting in, always remain positive throughout the interview: there will be time to discuss concerns later</li>
		<li>At the end of the interview, always be positive if asked about your interest in the job</li>
	</ul>
</div>
<div class="ebSection">
	<h1>After the Interview</h1>
	<p>
  	It's worth making notes immediately afterwards on what you thought went well and what didn't.
  	Experience always enhances performance, so make the most of the meeting.
	</p>
	<ul>
		<li>Write down any questions you failed to ask, or that occurred to you afterwards</li>
		<li>Note the difficult questions, so you can prepare answers next time</li>
		<li>Make a note of who you met in the interview, so you remember if you go back for a further interview</li>
		<li>Follow up with a brief thank you mail, reiterating your interest in the position. If you come up with
			additional information which might help the company in your favour, offer it here.</li>
	</ul>
