<h2>How to Win Friends and Influence People</h2>
<p>
	The book <i>How to win friends and influence people</i> has been influential in business and politics
	for nearly 100 years. It offers brilliantly simple insights into improving all personal relationships,
	perhaps most importantly with clients and customers, which anyone can use.
</p>
<p>
	The author, Dale Carnegie, found he was having trouble selling to some customers, and by
	applying some thought and different approaches, he could be much more successfull. He soon
	realised these techniques could be applied to personal relationships as well, and that there
	was a huge demand from people who wanted to improve some relationships in their business or personal
	lives.
</p>
<p>
	"Dale Carnegie offers practical advice and techniques for how to get out of a mental rut and make life
	more rewarding. His advice has stood the test of time and will teach you how to: make friends quickly
	and easily, increase your popularity, win people to your way of thinking, enable you to win new clients
	and customers and, arouse enthusiasm among your colleagues." (Review in the Guardian)
</p>
<p>
	"It is humanity and deepest sincerity that connects human beings together;
	and if they happen to be seller/buyer then that's just the way things are." (Customer review from Amazon)
</p>
