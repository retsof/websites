<h2>Why You Need Good Listening Skills</h2>

<p>The ability to listen carefully will allow you to:</p>
<ul>
	<li>better understand assignments and what is expected of you</li>
	<li>work better in a team-based environment</li>
	<li>resolve problems with customers, co-workers, and bosses</li>
	<li>find underlying meanings in what others say</li>
</ul>

<p>How to listen well:</p>
<ul>
	<li>maintain eye contact</li>
	<li>don't interrupt the speaker</li>
	<li>sit still</li>
	<li>repeat instructions</li>
	<li>ask appropriate questions when the speaker has finished</li>
</ul>

