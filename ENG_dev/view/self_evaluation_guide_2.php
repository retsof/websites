	<h2>Mentors and Personal Coaching</h2>
	<p>
	Mentors or personal coaches can improve confidence and lead to job opportunities you had not considered.
	Personal coaches can be hired to fill gaps in your experience or ability, perhaps with public speaking,
	prioritising, management and career planning.
	</p>
	<p>
	Perhaps the best option is to find a mentor, although they can be difficult to find. They will be a
	person with lots of experience in your field or a similar area. They must like you and be
	enthusiastic about your career, because they will be doing this for the enjoyment of seeing
	your career grow. A good mentor is a coach, always challenging you, inspiring you and demanding that you
	do your best. They are familiar with a range of professional opportunities and are guides who enjoy helping
	others and love their work. They will also have a large network that will assist you.

