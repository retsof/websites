	<h1>The recruitment process into the EU Institutions</h1>

	<p>
  	Whenever there are new permanent positions to be filled in the EU Institutions, a recruitment competition
  	(also widely known by its French name "concours") is held. There are usually 3 stages to each competition:
	</p>
	<ul>
		<li>Pre-selection tests</li>
		<li>Written tests</li>
		<li>Interviews</li>
	</ul>
	<p>
  	Examination centres are frequently set up in each member state - sometimes more than one centre in
  	larger countries - but the interviews are generally held in Brussels or Luxembourg.
	</p>
