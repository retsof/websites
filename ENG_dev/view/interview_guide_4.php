	<h1>Short phone interviews</h1>
	<p>
  	Contacting organisations and companies is inevitable when searching for job.
  	Whether you apply unsolicited, or you ask questions about an interesting job or you call the HR
  	department after having sent application and CV, it is important to keep detailed track on the status
  	of all your contacts. Most probably you are applying for several jobs at the same time like everybody else.
	</p>
	<ul>
		<li>Keep a job search diary to keep records of those companies, which you have contacted</li>
	</ul>

	<h2>Don’t underestimate unsolicited phone calls</h2>
	<p>
		Inspirational phrases you could use for unsolicited calls:
	</p>
	<ul>
		<li> Hello, my name is Charles Dupont. Could you please tell me who hires your company’s staff and put me through to them?</li>
		<li> Hello Cecile, my name is Charles Dupont.</li>
		<li> I’ve had a few years experience in political organisations, and I’d like to meet with you and talk about possible job opportunities with your company.</li>
		<li> Would you mind if I come in and talk with you anyway, in case you have an unexpected vacancy?</li>
		<li> Are you aware of any other departments who may need staff at the moment?</li>
		<li> Do you have a contact name there? Would you mind if I said you suggested calling them?</li>
		<li> Are you aware of your company changing structure or planning to establish new departments?</li>
		<li> Have you heard that your competitor, so and so, are creating an office in Brussels to be more present before the political decisions?</li>
		<li> I’d like to check back with you in a week or two just to see if anything has come up.</li>
		<li> In the meantime, I’ll pop my CV in the mail to you.</li>
		<li> Thank you very much for your time, Cecile.</li>
	</ul>
</p>

