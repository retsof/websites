	<h1>From the interviewer’s point of view</h1>

	<p>
  	Interviewers have a number of set questions which they ask every candidate.
  	These questions are based around the requirement of the job. By asking the same questions,
  	they can draw a direct comparison between each candidate.
	</p>
	<p>
  	The interviewer will ask questions, which are open, for example how, when, what, why etc. giving the
  	candidate the opportunity to answer fully, rather than responding with a simple ‘yes’ or ‘no’.
  	They will avoid interrupting your answers or leading them in any direction. Very often they summarise
  	the responses back to the candidate, before making notes, to ensure that they have fully understood.
	</p>

	<p>
  	Normally the interviewer shouldn’t be tempted to talk too much to fill any potentially uncomfortable
  	moments of silence. The silence can from the interviewers point of view be a very effective way of
  	encouraging the candidate to offer up more information.
	</p>
	<p>
  	This whole pattern can seem artificial at first, but it actually gives you the chance to show what
  	you’ve got. So you must prepare.
	</p>

	<h2>Stand out from the crowd</h2>

	<p>
	Prove that you are the right kind of person for the job. Employers like motivated people
	- so show your initiative. Qualifications are important, but employers also look for additional qualities.
	</p>
	<p>
  	Employers are looking for people who:
	</p>
	<ul>
		<li>are motivated and keen</li>
		<li>can work in a team</li>
		<li>can learn new skills</li>
		<li>are well presented</li>
		<li>are confident</li>
		<li>will put in time and effort</li>
		<li>have a good work history</li>
		<li>have relevant experience</li>
	</ul>
	<p>
	Interviewers know though experience and research that what appears to be a more informal chat
	than a ‘grilling’ tends to put a candidate at ease to get a more honest response.
	This gives valuable insight into the candidate’s personality traits, rather than simply an understanding
	of their skills and experience.
	</p>

	<div class="floatclear"></div>

	<p>
  	So remember that the recruiter will be noticing your body language:
	</p>
	<ul>
		<li>The initial shaking of the hand and introduction tell so much about you</li>
		<li>As does your ongoing seating position, use of hands and use of eye contact</li>
	</ul>
</div>

