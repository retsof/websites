  <h1>Quick Guide to Creating a Successful Job Advert</h1>
  <br>

  <h2>Job advert checklist</h2>

  <p>
  Check that your advert covers all the below items before you post your job advert,
  to get the basics right.
  If you have time, read on in the guide to ensure you attract the right candidate.
  </p>
  <br>
  <h2>Job advert essentials</h2>
  <ul>
    <li>Precise job title</li>
    <li>Name of employer (or recruitment agency)</li>
    <li>Job location</li>
    <li>Whether the role is full-time, permanent or a short-term contract</li>
    <li>Publishing date and application deadline</li>
    <li>Job and advert reference number if necessary</li>
    <li>Have clear text, layout, and a professional image</li>
    <li>Branding should be present but not overbearing</li>
  </ul>
  <br>

	<h2>Job description</h2>
 <ul>
    <li>Short description of business/organisation, and division activity, market position and aims</li>
    <li>To whom the position reports - or other indication of where the role is in the organisation</li>
    <li>Outline of job role and purpose - set up in a list of points if possible</li>
    <li>Indication of size, responsibility, timescale, and territory of role</li>
    <li>List of firm requirements for the job (as bullets)</li>
    <li>Outline of ideal candidate profile</li>
    <li>A separate list of other skills that would be advantageous to have</li>
    <li>Indication of qualifications and experience required</li>
    <li>Salary range</li>
    <li>Other package details (pension, car etc.)</li>
    <li>Career advancement opportunities</li>
    <li>Equipment and systems to be used in the performance of the job, if necessary</li>
 </ul>

  <div class="floatclear"></div>
  <br>

	<h2>Response and other information</h2>
 <ul>
    <li>Response and application instructions</li>
    <li>Explanation of recruitment process</li>
    <li>Perhaps interview dates</li>
    <li>Contact details as necessary</li>
    <li>Equal opportunities statements</li>
 </ul>
  <br>

  <p>
See the job ad as your working document; <i>the most effective tool for finding the right person.</i> If you work this way, all elements should automatically get prioritised right.
  </p>

