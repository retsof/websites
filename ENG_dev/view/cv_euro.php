	<h1>CVs in the European jobmarket</h1>

	<h2>Do you need to use a specified CV format?</h2>
	<p>
		The European Institutions have developed a standard CV template to try to make all applicants use,
		called Europass. The CV is filled in through an online form and consists of a CV and a language portfolio.
		The Europass is meant to help you find a job, enroll in education programs, etc. in order to build a
		career within the European Institutions. The idea is to help with mobility for both employers and employees.
	</p>
	<p>
		So far the CV template doesn't seem to be systematically used, and is not used by that many other employers
		than the EU institutions themselves. We would only recommend using it when this is specifically requested.
	</p>
	<p>
		The European Commission prefers their "EU CV Online", which is yet another CV database solution.
		EPSO also has its own format for CVs submitted by contract agents and the Commission's Directorate General for
		Research Technology & Development (DG RTD) has the ELSA database for prospective candidates established for the
		Sixth Framework Programme. The different CV formats are not yet consolidated into a common CV format,
		and there is no common database. The Institutions and Commission are apparently working on this, but we do not
		know when it is going to succeed. <a href="http://ec.europa.eu/civil_service/job/cvonline/index_en.htm" target="_blank">See more about EU CV Online here</a>.
	</p>

	<p>
	The Europass holds the same elements as most other standard CV formats, and is fairly easy to create.
	But only some of the EU institutions are using this format, so should not be considered as a shortcut
	into the European Affairs jobmarket in general, with the possible exemption of contractors for the EU
	tenders, who sometimes specifically ask applicants to use this format.
	</p>

