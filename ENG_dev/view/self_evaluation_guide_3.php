  <h2>Ways to Distinguish Yourself</h2>
  <p>
  	Being part of the commodity crowd erodes your professional value.
  	You are probably currently just another cog in the wheel,
  	another disposable footsoldier in the army of suits.
  	How do successful people pull off that promotion and avoid being made redundant?
	One reason is they are distinctive and stand out from the crowd.
	To do this, you need to think and be different.
  	In other words, you need to distinguish yourself.
  </p>
  <p>
  	Below are some of the ways you can fairly easily do things that will make
  	you stand out as an employee, client, team member and business partner.
  </p>
  <ul>
    <li>Care about your work, and be passionate about it</li>
    <li>Underpromise and overdeliver, setting high standards for yourself</li>
    <li>Ask for help and don't hoard information</li>
    <li>Dream big ideas and think long term</li>
    <li>Build strong relationships</li>
    <li>Plan your outcomes not your activities</li>
    <li>Embrace uncertainty, and get back on your feet fast when you have a setback</li>
    <li>Learn to lead in a volunteer project</li>
  </ul>

