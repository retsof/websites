	<h1>Career Planning</h1>
	<h2>Set Smart Goals</h2>
	<p>
	Setting yourself short, medium and long term goals makes the really big things easier to get to.
	Decide what you want, when you want it and how you're going to get there. Your goal doesn't have to
	stay a pipedream.
	</p>

	<p>
	The easiest way to do this is to establish a series of SMART goals. All you have to do is write it
	down in a place where you can see it when you wake up, during the day and before you go to bed.
	SMART stands for Specific, Measurable, Achievable, Relevant and Time-bound, and is a useful reminder
	of how to write a top quality goal.
	</p>

	<ul>
		<li>Specific: your goal should have its expected outcome stated as simply, concisely and explicitly as possible. This answers questions such as; how much, for whom, for what?</li>
		<li>Measurable: a measurable goal has an outcome that can be assessed either on a sliding scale (1-10), or as a hit or miss, success or failure.</li>
		<li>Achievable: an achievable goal has an outcome that is realistic given your current situation, resources and time available. Goal achievement may be more of a "stretch" if the outcome is tough or you have a weak starting position.</li>
		<li>Relevant: a relevant goal should help you on your mission or your "bigger" objectives.</li>
		<li>Time-bound: a time-bound goal includes realistic timeframes.</li>
	</ul>

	<p>
 	This idea and the text comes from the website: <a href="http://www.uncommon-knowledge.co.uk/goal_setting/smart_4.html" target="_blank">Uncommon Knowledge</a>, where you can find lots of examples.
	</p>

