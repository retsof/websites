	<h1>Grasp your target audience</h1>

	<h2>Pass the first filter</h2>
	<p>
	Your CV will probably be read first by recruitment agents, or human resources staff
	and not the person you would work for. Therefore you must aim to get past these people
	to stand any chance of being interviewed.
	These people may have hundreds of CVs to look through.
	Their task will be to filter all the received CVs down to only a handfull to pass on
	to the hiring manager. They look for any excuse to reject your CV - don't give them
	any chance to do this.
	</p>
	<ul>
		<li>Make sure your key points are highlighted </li>
		<li>Have a summary at the top of the CV (read on for more details)</li>
		<li>Spend time on the cover letter, making sure you cover all key points - to avoid being rejected before the CV is even looked at</li>
		<li>Spell check thoroughly</li>
	</ul>

	<div class="floatclear"></div>

	<h2>From the recruiter's point of view</h2>
	<p>
	The recruitment agent or human resource person will not read three pages of dense text to find out if you are suitable.
	They may have 200 CVs to skim for one position, and will also be recruiting for other positions.
	Their first task is to cut the CVs down to a managable number for detailed analysis.
	You must convince them you are a possible candidate within a few lines and easily scanned highlights.
	</p>
	<p>Imagine a scenario where your CV is lying in a pile of ten CVs. How can you avoid the bin?</p>

	<p>
	Remember that the person interviewing may not be the person who has chosen the candidates for interview,
	and may not have much time to read your CV before the interview. Sometimes CVs are passed by human resources to the
	interviewer only hours or minutes before the interview.
	</p>

	<h2>The cover letter</h2>
	<p>
	The cover letter can get you rejected, even if you are a strong candidate.
	Make sure that the cover letter puts you in the pile for further consideration.
	</p>
	<ul>
		<li>Refer to the job you are applying for. Be as specific as possible, mentioning the job title, and where you saw the ad.
	The human resources staff may well be recruiting for many positions, and they will appreciate you saving them time</li>
		<li>List a few reasons why you are suitable for the position, and draw attention to parts of your CV that are especially suited to this job.</li>
		<li>Explain why you particularly want to work for their organisation</li>
	</ul>

	<h2>In summary</h2>
	<p>
	your CV should:
	<ul>
		<li>have a summary and cover letter</li>
		<li>be easy to navigate</li>
		<li>be written in clear, spellchecked language</li>
		<li>be layouted in a conservative and trustworthy manner</li>
	</ul>
</p>
