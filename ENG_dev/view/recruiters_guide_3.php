  <h1>Organising Your Recruitment</h1>
  <br>

  <h2>Defining job function and title</h2>
  <br>

  <p>
Recruitment is not just carried out to meet immediate vacancies but should be seen as part of an overall organisational strategy for resourcing personnel. See if you can discuss the job role with involved departments, managers, colleagues, leaving job holder etc., to get the whole picture.
  </p>

  <ul>
    <li>If the candidate is to <b>replace a leaving job holder</b> (in other words an existing job role), then take the opportunity to re-evaluate the job scope</li>
    <li>If the candidate is to fill out a <b>new job function</b> with many unknown factors, then sketch out the overall lines and objectives: be honest about uncertainties</li>
    <li>If the candidate is to fill out <b>a temporary vacancy</b> or an internship, then explicitly define expectations and job tasks. Your permanent job may be attractive to ambitious people who will use it as a stepping stone to more prestigious jobs. Fine, but if you want to avoid any lack of involvement, you need to specify this in your job ad.</li>
  </ul>
  <br>
  <p>
If the job title does not explicitly describe the job function, then use a strapline to do so. You may have to work a lot with the job title, so that it is not obscure and doesn't convey what the job function is.<br>
Also try to incorporate something new, innovative, exciting, challenging in the role - people are attracted to new things
  </p>

  <p>
More on financial consequences of hiring the wrong person:<br>
<a href="http://generatorgroup.blogs.com/generator_weblog/2006/08/avoid_costly_hi.html" target="_blank">Avoid Costly Hiring Mistakes by Improving Selection Performance</a>
  </p>
  <br>

  <h2>Job requirements</h2>
  <br>

  <p>
The job description must be in line with reality. In the eyes of the new candidate, and the colleagues, the official job advert remains the most important document for the job role (together with the contract). So you must take it seriously, otherwise you might put performance of the new professional at risk.
  </p>

  <p>
In other words your choice of wording is important: too complex and you could give the impression of inaccessibility, too lightweight and you may lose credibility.
Ask colleagues to compare your proposed wording with their experiences of the company and the job.
  </p>
  <br>

  <h2>How many and which applications will you receive?</h2>
  <br>

  <p>
An instrument to control the amount of applications is the job requirements. The more specific your job requirement list, the fewer, better targeted responses you are likely to receive.
  </p>

  <ul>
    <li>If the role is specialist and senior, you may prefer to receive five excellent applications rather than fifty average ones</li>
    <li>For a graduate trainee position you may prefer a bigger choice</li>
  </ul>

  <p>
Writing a goal oriented job description will attract potential candidates who want to grow within the position, and learn how to make larger contributions to your organisation. See the difference between these two phrases:
  </p>

  <ul>
    <li><i>Is responsible for the preparation and follow-up of financial management and internal control</i></li>
    <li><i>Takes responsibility for developing a sound financial management and internal control to save money for the agency</i></li>
  </ul>

  <p>
In the same way, clearly defined job <b>role objectives</b> will, instead of a list of <b>job responsibilities</b>, turn focus more towards the personal qualities than the skills. This is critical for high responsibility jobs. But perhaps to be avoided in more administrative functions.
  </p>
  <br>

  <h2>Talking to your ideal candidate</h2>
  <br>

  <p>
Build a mental picture of the sort of person you, and the team, see fitting in.
Write as if you were speaking to your imaginary candidate.
Use language that your reader uses, and describe the job as practically as possible.
Refer to the reader as 'you' and use the second person ('you', 'your' and 'yours' etc)
in the description of requirements and expectations. It gets the reader involved.
  </p>
  <p>
	The job description should be as practical as possible. Make it short. Cut down on formalities (and explain them later to the candidate). Try to omit any unnecessary articles such as "a", "an" and "the". Mark for example the difference between these two:
  </p>
  <ul>
    <li><i>Managing the development team: this includes organising and planning the work of a team of members, reporting on the work progress to the project managers, researching and selecting technologies needed to implement services, keeping the development team up-to-date with state of the art...</i></li>
    <li><i>Managing development team; organising and planning work team members, reporting on work progress to project managers, researching and selecting technologies needed to implement services, keeping development team up-to-date.</i></li>
  </ul>

  <p>
Remember that there is a major difference between an online ad and print version. Online job boards force users to read much more from top and downwards. Therefore the job ad must quickly get to the point. If it doesn’t grab attention, the race is lost before it begins.
  </p>

  <p>
How to get your job advert noticed:
  </p>

 <ul>
    <li>Don’t use long words where short ones will do</li>
    <li>Cut out words where possible</li>
    <li>Use active forms instead of passive</li>
    <li>Strong verbs and specific nouns give energy</li>
    <li>Avoid over-designed graphics and extravagantly presented layouts</li>
 </ul>


