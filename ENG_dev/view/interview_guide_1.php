	<h1>Research the company</h1>
	<p>
		The most important thing you must do to prepare for an interview is to study the company in advance.
		By going to the interview prepared and able to demonstrate understanding of the company’s business,
		you will show that you are actively interested, rather than simply seeking any job anywhere.
		This gives you a critical advantage.
	</p>
	<ul>
		<li>Look carefully at the company’s website. Read their press releases, find out their mission statement,
			and see how they describe themselves.</li>
		<li>Research using the internet and news media, to see what the current issues are for the company.</li>
		<li>Find out who the company's competitors are, and what other companies or organisations are doing
			in the same area. Try and think why you prefer this company rather than the competitors.</li>
		<li>Ask friends, colleagues and your network to get a behind-the-scenes view.</li>
		<li>Contact the firm's marketing department for up-to-date literature</li>
	</ul>
</div>
<div class="ebSection">
	<h1>Research the interview process</h1>

	<p>
  	Remember that big public institutions and recruitment processes in
  	general can be bureaucratic and slow. Some organisations use recruitment agencies,
  	which might either speed up or slow down the process. One can easily lose courage along the way.
	</p>
	<h2>Make sure you spend your energy on the key people in the process</h2>

	<p>
  	Who are the key people? The recruitment manager within the organisation, and of course your potential manager.
	</p>
	<p>
  	Research the interviewers. If you are interviewed by a person who is listed on the internet,
  	find out as much as you can about him and his work. Save your energy for the key people if having
  	many interviews at the same time. It could be fatal to burn off your precious energy
  	with human resources or a recruitment agent, and then be tired for the most important interview with
  	your potential manager.
	</p>
	<h2>Find out about the interview process</h2>
	<p>
  	If possible, find out the format of the interview process beforehand. You should avoid contacting
  	the interviewers directly to ask process questions, but you may be able to find out by contacting the HR
  	department or recruitment agency with a polite e-mail or phone call. Perhaps you know someone who has already been interviewed
  	and can ask them. Finding out this can make your interview a much more relaxed:
	</p>
	<ul>
		<li>How many interviews will there be?</li>
		<li>Does the firm carry out psychometric testing?</li>
		<li>Who will be conducting the interviews?</li>
	</ul>
</div>
<div class="ebSection">
	<h1>Prepare questions</h1>
	<p>
  	Think about questions you'd like to ask about the specific job you have applied for,
  	and also about the company in general. Take a written list of questions with you.
		<!--Prepare questions which invite thought and comment to the
  	interviewer, more than those which request specific detail. -->
  	This is also your opportunity
  	to show you are serious, and demonstrate your understanding of the organisation.
	</p>

	<p>
  	Ideas of questions to ask your interviewer:
	</p>
	<ul>
		<li>How would you describe the organisation culture?</li>
		<li>What are the organisation's and the department’s major business objectives in the coming year?</li>
		<li>What does the organisation do to support employees in their career development?</li>
		<li>How has this vacancy come about?</li>
		<li>What can I expect to be involved in during my first six months of joining?</li>
		<li>What are the key challenges of this role?</li>
	</ul>

	<h2>Salary</h2>
	<p>
	Although you've thought about the salary side of things, always allow your interviewer to initiate
	these discussions. This might not occur during the first interview. Negotiate as late as possible:
	you will have most influence when the recruiter already wants you.
	</p>

</div>
<div class="ebSection">
	<h1>Will you fit in at all?</h1>

	<p>
  	During this preparation, don’t forget that you may not be quite right for one job, but perfect for another.
  	Don’t be afraid to cancel the interview if you discover that the company doesn’t have a culture that
  	suits you for example.
	</p>
	<p>
  	Companies differ. One person's dynamic and exciting environment may seem competitive and back-biting
  	to another. It's your opinion that matters. A company's literature and also your interviewer will
  	provide clues on how to convey the impression that you'll fit in. But bear in mind that before accepting
  	a position you must believe that you really will fit in. If the organisation's culture is very different
  	from your own, it's unlikely you'll be successful.
	</p>

