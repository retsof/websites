  <h1>Putting your job advert into words</h1>
  <br>
  <h2>Avoid unsuitable applications</h2>
  <p>
	At <i>EuroScienceJobs.com</i> we see a lot of job ads, and it?s suprising how many job ads that fail to provide basic information. And therefore recruiters waste a lot of time analysing unqualified applications, and perhaps having to re-publish the advert.
  </p>
  <p>
	To get only the best applications, and reduce the number of rejected applicants, try these:
  </p>
  <ul>
    <li>Have very clear requirement list, and make it as short as possible</li>
    <li>Have a separate ?would be advantageous to have? list</li>
    <li>Indicate that ?only short listed candidates will be contacted?</li>
    <li>Write clearly where the job is located geographically</li>
    <li>Specify if applicants must be EU nationals, or must have a valid work permit</li>
    <li>Define clear posting and closing dates</li>
    <li>Make posting, mailing and telephone contact details easy to find</li>
  </ul>

  <p>
Prevent unqualified job seekers from sending useless applications by having a statement that explains that your job requirements are firm. For example:
<br>

<i>"Please read the qualifications for this position carefully. We will only consider those who meet the listed requirements."</i>
  <br>

This won't stop everyone, but it will deter people who are unsure whether you are serious about your stated requirements.
  </p>

  <p>
A clear statement of how you are going to reply to applicants will reduce you work task when handling the incoming CVs. Write for example:
<br>
<i>"The deadline for applications to reach us is 30 March. If you have not heard from us by 15 April, please assume that your application has not been successful."</i>
  </p>
  <br>

  <h2>Clarity means reaching the right people</h2>

  <p>
Give candidates the information they need for applying. If an employer is vague or incomplete, the job seekers you want may be less likely to apply, and those who are unsuitable may be encouraged to apply. In fact the hired candidate might have a long term motivational problem, because of an unclear job description.
  </p>
  <p>
	Here is some clear job description sample (taken from <i>WHO)</i> ? it explicitly divides job tasks into clear groupings:
  </p>
  <ul>
    <li><i>Negotiate agreements with external publishers (both nonprofit and for-profit) for the translation and publication of information materials in appropriate languages</i></li>
    <li><i>Cultivate and expand partnerships worldwide with both nonprofit and for-profit bodies in the area of publishing</i></li>
  </ul>
  <br>

<p>Things to avoid when wording your job description:</p>
  <ul>
    <li>clever or obscure headlines</li>
    <li>coded and idiosyncratic communications</li>
    <li>coded and idiosyncratic communications</li>
    <li>uninspiring descriptions of roles and ideal candidates</li>
    <li>too much emphasis on the job and not enough on the person</li>
    <li>words which are subject to differing interpretations, for example: "frequently," "some," "complex," "occasional," and "several"</li>
  </ul>

  <p>
Remember, it's a bigger decision to spend years or a lifetime working for your company than it is to purchase your service. The rule is to excite and motivate candidates to apply to your job posting - not scare them away.
  </p>
