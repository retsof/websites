	<h1>Prepare your CV persona</h1>
	<p>
  	Interviews are always stressful, even for job seekers who have gone on countless interviews.
  	The best way to reduce the stress is to be prepared.
	</p>

	<p>
  	In short, here are the key factors:
	</p>
	<ul>
		<li>Take time to consider obvious questions you might be asked and practice your responses</li>
		<li>Look carefully at the CV you have sent to the company</li>
		<li>Be ready with plenty of examples to illustrate your skills, and how you could contribute to the company</li>
	</ul>

	<p>
  	During the course of the interview you might be asked a number of fairly standard questions.
  	Let’s take a quick look on some obvious ones:
	</p>
	<ul>
		<li>Tell me something about yourself</li>
		<li>What do you think are your key strengths and weaknesses?</li>
		<li>Describe some major achievements in your career</li>
		<li>If you could change anything about your career so far, what would it be?</li>
		<li>Describe your greatest career challenge so far</li>
	</ul>

	<h2>Know your CV by heart</h2>
	<p>
   	You must know your CV inside and out. Be prepared to explain any decisions you've made on previous
   	career moves. Prepare to be confident about talking about your key achievements. But also think hard
   	about previous interviews you have had where some of the questions asked proved difficult,
   	or some of your own answers seemed unsatisfying.
	</p>

	<p>
  	Take a look at these frequently asked interview questions:
	</p>
	<ul>
		<li>What brings you to the job market at this point in your career?</li>
		<li>Why would you like to work for this company in particular?</li>
		<li>What attracts you to this role?</li>
	</ul>

	<div class="floatclear"></div>

	<h2>Weaknesses in your CV</h2>
	<p>
  	Try and identify areas of weakness in your CV. Get friends to have a look at your CV, and
  	ask them to honestly point to areas which may concern a potential employer. You then need to
  	prepare to be asked about these areas, and have explanations ready.
	</p>
	<p>
  	Weaknesses to prepare for:
	</p>
	<ul>
		<li>Gaps in your work history</li>
		<li>Short periods with an employer</li>
		<li>Not being promoted</li>
		<li>Weak exam results or degree results</li>
	</ul>

</div>

