<div class="ebSection">
	<h1>This is your life</h1>
	<p>
	Let’s sum up the key preparation you can do:
	</p>
	<ul>
		<li>Have the basis of your response thought out in advance</li>
		<li>Be honest</li>
		<li>Don’t position yourself as a team player if you are a loner. It may get you the job, but in the long term you will be unhappy.</li>
		<li>Don’t excuse yourself making a positive statement into a weakness</li>
		<li>Identify your personal strengths and weaknesses, e.g. through <a href="self_evaluation_guide"><?php print WEBSITENAME; ?>’ Self-Evaluation Guide</a></li>
		<li>Make your statements as relevant to the job you have applied for as possible</li>
		<li>Keep your answers as short and succinct as possible, using specific examples or scenarios to demonstrate your experience, ability and knowledge</li>
		<li>Ask friends to give you mock interviews in advance</li>
		<li>Remember what a world-class sportsman once said: 'The more I practice, the luckier I get'.</li>
	</ul>
	<p>
  	Do some more preparation for frequently asked interview questions:
	</p>
	<ul>
		<li>What do you want from your next role?</li>
		<li>Name key things that motivate you?</li>
		<li>Describe your preferred company culture</li>
		<li>Where do you see yourself in five year's time?</li>
		<li>What really annoys you in the workplace?</li>
	</ul>
