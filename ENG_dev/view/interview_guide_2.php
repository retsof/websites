	<h1>Tough Questions</h1>
	<h2>Why are you leaving your job?</h2>
	<p>
	Some questions are harder than others. It depends on the weak spots in your CV and your personality.
	Consider for instance this question: <i>Why are you leaving your job?</i>
	</p>
	<p>
	You must be direct and focus your interview answer on the future. Especially if your leaving wasn't
	under the best of circumstances.
	</p>
	<ul>
		<li>See a lot of <a href="http://jobsearch.about.com/od/interviewquestionsanswers/a/interviewleave.htm" target="_blank">possible answers to that question</a> here</li>
		<li>And even more help  <a href="http://www.career-intelligence.com/transition/MostDifficultInterviewQuestions.asp" target="_blank">here</a> </li>
	</ul>
	<h2>Why were you fired?</h2>
	<p>
	Perhaps you are dreading this question.
	A possible answer to this could be: "I usually have good relationships with everyone,
	but this case was an exception. We just didn't get on well. I'm not sure why."
	</p>
	<ul>
		<li>See a lot more
			<a href="http://jobsearch.about.com/od/interviewquestionsanswers/a/interviewfired.htm" target="_blank">answers to that question</a> here</li>
	</ul>
	<h2>Other tough questions</h2>
	<p>
	If you haven’t done it already, you must run through the lists of uncomfortable questions you might be facing.
	Prepare your answers, otherwise you may struggle.
	Often the hard ones are about personal relationships:
	</p>
	<ul>
		<li>How would your colleagues describe you?</li>
		<li>Describe your management style</li>
		<li>What do you look for in a manager?</li>
		<li>What would you say about your current employer?</li>
	</ul>
