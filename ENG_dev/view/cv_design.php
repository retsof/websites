	<h1>Classic tricks for CV design</h1>

	<h2>Keep your CV short</h2>
	<p>
  	Whether you are an experienced professional and have worked for several organisations over the years,
  	or a recent graduate - it is often a temptation to make your CV too long. Rule of thumb:
	</p>
	<ul>
		<li>A junior employee's CV should be one or two pages</li>
		<li>An experienced employee's CV should be no more than four pages - usually three pages is sufficient</li>
		<li>Many details are irrelevant for job experience more than five years ago - remove as much as possible from those jobs
			(for example, five years old versions of Word and Excel are not important).</li>
	</ul>

	<h2>Bullet points - the miracle cure</h2>
	<p>
  	Use bullet points wherever possible. They help make sentences short and easy to read,
  	which means a recruiter can scan quickly and pick out the important points.
  	It also forces you to think of the order in which points are presented.
	</p>

	<h2>No errors</h2>
	<p>
		Your CV can easily fail to get you the job, so be careful.
	</p>
	<ul>
		<li>Always spellcheck</li>
		<li>Read your CV carefully for grammatical mistakes, and check dates of employment and other important facts</li>
		<li>Get your CV read by someone else to check for obvious mistakes too</li>
		<li>If there are obvious mistakes in your CV, recruiters will see this as lack of care.
			This implies you are a sloppy person, and you may be rejected because of spelling or grammar mistakes!</li>
	</ul>

	<h2>Avoid fancy designs and graphics</h2>
	<p>
  	In most cases a fancy design is an annoyance to the recruiter and will probably hide the message you are trying to get across.
	</p>
	<ul>
		<li>Use a standard font such as Times Roman or Arial</li>
		<li>Don't make the font too small - the CV may be faxed and photocopied, and if your text is unreadable, it will end in the bin</li>
	</ul>

	<h2>Layout your CV to prevent photocopy and fax chaos</h2>
	<p>
  	Always have "page X of Y" and your name and address on each page of your CV.
  	It is easy to mislay one page of a CV, or to not know which order the pages come in.
  	Remember most CVs are emailed, photocopied or faxed at some point, and they can get jumbled up on the printer.
	</p>
	<p>
  	Think visually - perhaps you edit your CV only on-screen. Print it out and have a fresh look.
  	This is what the interested recruiter will do.
	</p>

	<h2>Tone of communication</h2>
	<ul>
		<li>Be honest. During interview you may be asked to explain any aspect of your CV. Make sure you can. If not your credibility is lost.</li>
		<li>Be positive. This is a selling document. Use words that have impact: 'managed' is stronger than 'supervised', 'negotiated' is stronger than 'facilitated'.</li>
		<li>Don't include your current salary. The best time to talk about this is at the end of the selection process, when you are sure that the employer wants you.</li>
	</ul>

	<h2>Contact Information</h2>
	<p>
  	There is absolutely no reason to make contacting you difficult for the potential employer,
  	yet so many CVs have no contact information except a home phone number.
	</p>
	<ul>
		<li>Give the potential employer your mobile phone number, even if you have to buy a mobile for your job search</li>
		<li>Have a header on each page of your CV with all contact details</li>
	</ul>

	<h2>List quantifiable achievements</h2>
	<p>
		Here are a few examples of achievements. If you can, briefly explain how you managed it.
	</p>
	<ul>
		<li>Made money or made sales for the organisation</li>
		<li>Completed a project on time, to budget</li>
		<li>Managed a team in difficult circumstances</li>
		<li>Helped save money or reduce costs</li>
		<li>Implemented processes that save time or increase work productivity</li>
		<li>Improved the company's competitive advantage in the marketplace</li>
		<li>Enhanced the organisation's image, or building organisation's reputation in its field </li>
	</ul>

	<h2>Covering letter must target the job role</h2>
	<p>
		Listen well to the recruiter's need. Read carefully the job advertisement, pick out the key words and write them in a list.
	<p>
	<ul>
		<li>Your covering letter should address as many of the points in the job advertisement as possible</li>
		<li>Your summary should address the central points. If it does not, you should consider having a different version of your CV for this sort of job</li>
		<li>If you don't manage to "tick all the boxes" in the covering letter and in the CV summary, then you may well be rejected at the first step</li>
	</ul>

