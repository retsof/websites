	<h1>Basic CV elements</h1>
	<h2>Pause before sending your CV</h2>
	<p>
		Found an attractive job? Before you send your old CV, check with our guide below, to make your CV bulletproof.
	</p>

	<p>
		First of all, consider using this universal CV structure:
	</p>
	<ol class="numbered">
		<li>Summary</li>
		<li>Work Experience</li>
		<li>Academic Qualifications</li>
		<li>Professional Qualifications and Training Courses</li>
		<li>Personal Information</li>
	</ol>

	<h2>Have a summary</h2>
	<p>
  	This is an important part of your CV. You probably only get one minute or fifteen lines of  text,
  	before the recruiter either rejects you or decides that you are a candidate. A summary enables you to get across all the
  	points that the recruiter is looking for as a minimum.
	</p>
	<ul>
		<li>The summary should be short, direct and business-like</li>
		<li>Bullet points are usually a good format for a summary</li>
		<li>The summary should list the key things that recruiters will be looking for for the specific job.
	If a key requirement for the job is not listed in your summary, you could be rejected even if it is shown elsewhere in the CV.</li>
		<li>The summary is your chance to highlight anything that will make you stand out from the crowd.
	For example, a particularly relevant university project.</li>
	</ul>

	<h2>Clean up your working experience</h2>
	<ul>
		<li>Usually, work experience is listed with a section for each employer</li>
		<li>Listed in reverse date order, i.e. with most recent first</li>
		<li>Each entry should have starting and ending month and year with each employer (you don't need the exact date)</li>
		<li>The employer's name is usually bold (and/or larger font)</li>
		<li>More recent jobs should have more detail than older jobs</li>
		<li>Keep the text brief and concise - there is no time for literary or poetic paragraphs</li>
		<li>Break each job into bullet points of the major points you need to get across
	(e.g. major project, major achievement, training courses, technical skills used)</li>
	</ul>

	<h2>An example job section:</h2>
	<p>
		<span class="bold">September 2002 - January 2004  XYZ Consulting</span>
	</p>
	<ul>
		<li>Consultant on pharmaceutical regulation accounts. Major clients included A, B and C.</li>
		<li>Responsible for patent regulation policy</li>
		<li>Co-headed working groups, wrote position papers, arranged meetings with MEPs, Commission officials</li>
		<li>Produced and delivered report on patient group response to Commission proposal on orphan drugs. Presented the results at pharmaceutical industry conferences</li>
		<li>Managed two junior consultants, one stagiare and one secretary</li>
	</ul>


	<h2>Academic Qualifications</h2>
	<p>
  	After a few years in work, the details of your university education become less important,
  	so list them below your job section. However, if you are looking for your first professional job,
  	the academic section should probably come before work experience.
	</p>

	<h2>Personal Information</h2>
	<p>
  	Personal interests can present you as a rounded person and fun to work with. But they can also get you rejected from the candidate list.
	</p>
	<ul>
		<li>Skills from hobbies (eg managing a club, mastering an instrument) can show off skills you have not been
  	able to yet use in a job or demonstrate on a CV. This can be crucial if you are a junior or straight from university,
  	and have little work experience.</li>
		<li>Personal information can make you stand out from the crowd, or it can get you rejected. Think of the reactions you often get, when telling strangers your interests.</li>
		<li>Discreet elegance can get you far. Having sporting or fitness interests can signify that you are disciplined and take good care of yourself.
	Team membership can indicate sociability and reliability</li>
		<li>Some outside interests may put recruiters off. For example, being active in a political or religious organisation should be ajusted with the mission of the organisation you are applying for.</li>
		<li>Including whether you are married, or have children, is not necessary. No employer can insist on this information, and it could possibly prejudice someone against you.</li>
	</ul>

	<h2>Photo included?</h2>
	<p>
  	This is generally not a good idea. If you for example are good looking you may think a photo will be an advantage.
  	But it could lead some people to say "he fancies himself, doesn't he?" or "she is too good looking and will distract a certain colleague".
	The style of the photo could lead to unexpected judgements about you. Remember you are trying to avoid being rejected,
	and photos provide more possible risks of rejection.
	</p>

	<h2>More than just one CV</h2>
	<p>
  	You may apply for different job types, because you haven't decided where your career lies yet.
  	However, if your CV is to cover different sorts of jobs, it may end up not being ideal for either type.
  	It is then a good idea to have different CVs for each type.
	</p>
	<ul>
		<li>Be very careful to note which CV type was sent to each job, so you can prepare using the right one.</li>
	</ul>
	<p>
	For example, you might be applying for an internship in a consultancy. Here it may be best to demonstrate that
	you can get started immediately, by highlighting similar work you have done elsewhere, and stressing perhaps you
	are experienced with office work and office software.
	This sort of CV should be very short, so your important points are highlighted.
	</p>
	<p>
  	If you were applying for an entry level position in a large management consultancy, then they are likely to have a
  	training programme. So they are not so interested in specific skills, but rather in indications of ability to learn
  	quickly and flexibility.
	</p>

	<h2>Max and min CV</h2>
	<p>
  	Another approach is to have a max and a min CV. As the years go by, and perhaps you make a few career switches,
  	it is always great to have kept detailled points from earlier on. Say you make your own business at a certain point,
  	and you might want to re-enter older elements into you newest CV. So to sum it up:
	<ul>
		<li>Your max CV contains all the skills, details and experience that you have accumulated during your whole career.
  	This is your also your back-up CV, so that you don't lose important points that you might have deleted in a recent CV
  	- and now you need them again.</li>
		<li>Your min CV is the actual curriculum for a specific job that you are currently applying for. Your min CV is put together from
	selected elements of you max CV, all depending on which type job you apply for.</li>
	</ul>
	<p>
  	The trick of cause is to write your max CV sections in a way, so that you can easily export elements to an actual (or min) CV,
  	without having to re-write the entire text.
	</p>

