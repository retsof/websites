	<h1>Your personality type</h1>
	<br>

	<h2>Myers-Briggs classification</h2>

	<p>
	Exploring yourself may help you to identify your general life style and your style in many fields of
	activity: work, business, education, communication, conflicts and teamwork.
	</p>
	<p>
	According to the Jungian, Myers-Briggs, typology all people can be classified using four criteria:
	</p>
	<ul>
		<li>Extroversion - Introversion</li>
		<li>Sensing - Intuition</li>
		<li>Thinking - Feeling</li>
		<li>Judging - Perceiving</li>
	</ul>

	<p>
	This model is used in a majority of personality tests. When you go through head hunter and recruitment
	interviews you might very well be exposed to tests like this. So knowing the drill beforehand, will
	help you go easier through.
	</p>
	<p>
	Here is a
		<a href="http://www.humanmetrics.com/cgi-win/JTypes2.asp" target="_blank">good and quick personality test</a> - and it's free.
	</p>
	<p>
	The test will give you some lengthy (though generic) interpretation of your personality type, which is
	very useful. But you can take it further into your career potentials.
	</p>
	<div class="floatclear"></div>
	<br>

	<h2>Proactive self assessment</h2>
	<p>
  	Once you know your type (stated as four letters, e.g. INFJ), you should search for this combination on Google.
  	Write the four letters in the search field, and voila! You will find lots of qualified interpretations
  	on your personality type; how to develop yourself, how to identify lifestyle and professional
  	orientation and not least: how to interact with people in daily working environment.
	</p>
	<p>
  	Knowing this much about yourself will make it a lot easier to update your CV and realistically sketch
  	out your strengths and weaknesses. It might be painful to discover that some dreams and ideas about
  	yourself do not lie within the prospect of your personality potential. On the other hand this is a
  	direct shortcut into succeeding by who you are, and not by who you would like to be.
	</p>
